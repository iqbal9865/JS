var num = 2000;
switch(num) {
    case 100:
        console.log('value is 100');
        break;
    case 200:
        console.log(`value is 200`);
        break;
    case 300:
        console.log(`value is 300`);
        break;
    case 400:
        console.log('value is 400');
        break;
    default:
        console.log("Number not found!");
        break;
}